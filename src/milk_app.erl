-module(milk_app).

-behaviour(application).

%% Application callbacks
-export([start/2, stop/1]).

%%====================================================================
%% API
%%====================================================================

start(_StartType, _StartArgs) ->
    {ok, Pid} = milk_sup:start_link(),
    Fun = fun
              ({M, A}) ->
                  apply(M, start, A)
          end,
    lists:foreach(Fun, [{network_tcp_sup, []}, {hot_update_sup, []}]),
    {ok, Pid}.


%%--------------------------------------------------------------------
stop(_State) ->
    ok.

